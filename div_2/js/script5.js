// Остаток от деления:
console.log('5 % 2 =', 5 % 2) // 1
console.log('8 % 3 =', 8 % 3) // 2
console.log('15 % 5 =', 15 % 5) // 0

var i = 1
i++ // i = i + 1
/* так же есть операторы --, +=, -=, и так далее */
console.log('i =', i)  // i = 2


i *= 3 // i = i * 3
console.log('i =', i) // 6

i /= 1.5
console.log('i =', i)  // 4

/* ++i и i++ так же как в php */
console.log('i', ++i)
console.log('i', i--)